"""M5 C1 Task 4 — core executor: four cleanup paths + error mapping."""

import asyncio

import pytest
from pydantic import BaseModel

from zygos.errors import ProviderError
from zygos.runtime.context import root_context
from zygos.runtime.events import InProcessEventBus
from zygos.tools.executor import execute_tool
from zygos.tools.types import BaseTool, ToolCall, ToolContext, ToolMeta




class _In(BaseModel):
    x: int


class _Out(BaseModel):
    y: int


def _ctx():
    return root_context(InProcessEventBus())


class SpyTool(BaseTool):
    """Records which phases ran; behavior is parameterized for each path."""

    meta = ToolMeta(name="spy", description="d", input_model=_In, output_model=_Out)

    def __init__(self, *, fail_execute=False, fail_prepare=False, bad_output=False,
                 raise_zygos=False, cleanup_raises=False):
        self.calls: list[str] = []
        self._fail_execute = fail_execute
        self._fail_prepare = fail_prepare
        self._bad_output = bad_output
        self._raise_zygos = raise_zygos
        self._cleanup_raises = cleanup_raises

    def prepare(self, ctx: ToolContext) -> None:
        self.calls.append("prepare")
        if self._fail_prepare:
            raise RuntimeError("prepare boom")

    async def execute(self, input: _In, ctx: ToolContext):
        self.calls.append("execute")
        if self._raise_zygos:
            raise ProviderError("zygos boom", provider="p")
        if self._fail_execute:
            raise RuntimeError("execute boom")
        if self._bad_output:
            return {"wrong": "shape"}
        return _Out(y=input.x)

    def cleanup(self, ctx: ToolContext) -> None:
        self.calls.append("cleanup")
        if self._cleanup_raises:
            raise RuntimeError("cleanup boom")


@pytest.mark.asyncio
async def test_happy_path_ok_and_cleanup_ran():
    tool = SpyTool()
    res = await execute_tool(tool, ToolCall(tool="spy", args={"x": 5}), _ctx())
    assert res.ok is True and res.output == _Out(y=5)
    assert tool.calls == ["prepare", "execute", "cleanup"]


@pytest.mark.asyncio
async def test_execute_raises_maps_to_execution_failed_and_cleanup_ran():
    tool = SpyTool(fail_execute=True)
    res = await execute_tool(tool, ToolCall(tool="spy", args={"x": 1}), _ctx())
    assert res.ok is False and res.error_code == "tool_execution_failed"
    assert "cleanup" in tool.calls


